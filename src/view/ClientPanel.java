    private void loadClientsData(int limit, int offset) throws SQLException {
        clients = clientController.getAll();
        List<Client> clients_temp = clientController.getSome(limit, offset);
        String[][] data = new String[clients_temp.size()][4];
        
        for (int i=0; i<clients_temp.size(); i++) {
            data[i][0] = clients_temp.get(i).getIdClient();
            data[i][1] = clients_temp.get(i).getName();
            data[i][2] = clients_temp.get(i).getAddress();
            data[i][3] = clients_temp.get(i).getTelephone();
        }
        
        String[] column = {"ID","Name","Address","Telephone"};
        jTableClient.setModel(new DefaultTableModel(data,column) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }  
        });
        
        jLabelTotal.setText(String.valueOf(clients.size()));
    }
    

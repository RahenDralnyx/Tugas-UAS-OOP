    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        String id = jTextFieldSearch.getText();
        Boolean found = false;
        int size = animals.size();
        int index = 0;
        for (Animal animal : animals) {
            if (id.equals(animal.getIdAnimal())) {
                found = true;
                break;
            }
            index++;
        }
        if (found) {
            try {
                index = ((size-1)-index);
                int page = (index/limit)+1;
                int row = index%limit;
                jLabelPage.setText("Page : "+String.valueOf(page));
                offset = (index/limit)*limit;
                loadAnimalsData(limit, offset);
                jTableAnimal.setRowSelectionInterval(row, row);
                
                int selectedRow = jTableAnimal.getSelectedRow();
                selectedId = jTableAnimal.getValueAt(selectedRow, 0).toString();
                jTextFieldType.setText(jTableAnimal.getValueAt(selectedRow, 1).toString());
                jTextFieldWeight.setText(jTableAnimal.getValueAt(selectedRow, 3).toString());
                jTextFieldAge.setText(jTableAnimal.getValueAt(selectedRow, 2).toString());
                jComboBoxGender.setSelectedItem(jTableAnimal.getValueAt(selectedRow, 4).toString());
                String ownerId = jTableAnimal.getValueAt(selectedRow, 5).toString();
                jTextFieldClientID.setText("(none)".equals(ownerId) ? "" : ownerId);
            } catch (SQLException ex) {
                System.getLogger(AnimalPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                clearForm();
            }
        } else {
            JOptionPane.showMessageDialog(this, "'"+id+"' "+"doesn't exist!");
            jTextFieldSearch.setText("");
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed



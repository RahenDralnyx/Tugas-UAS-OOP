    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        String id = jTextFieldSearch.getText();
        Boolean found = false;
        int size = doctors.size();
        int index = 0;
        for (Doctor doctor : doctors) {
            if (id.equals(doctor.getIdDoctor())) {
                found = true;
                break;
            }
            index++;
        }
        if (found) {
            try {
                index = ((size-1)-index);
                int page = (index/limit)+1;
                int row = index%limit;
                jLabelPage.setText("Page : "+String.valueOf(page));
                offset = (index/limit)*limit;
                loadDoctorsData(limit, offset);
                jTableDoctor.setRowSelectionInterval(row, row);
                
                int selectedRow = jTableDoctor.getSelectedRow();
                selectedId = jTableDoctor.getValueAt(selectedRow, 0).toString();
                jTextFieldName.setText(jTableDoctor.getValueAt(selectedRow, 1).toString());
                jTextFieldAddress.setText(jTableDoctor.getValueAt(selectedRow, 2).toString());
                jTextFieldTelephone.setText(jTableDoctor.getValueAt(selectedRow, 3).toString());
                jTextFieldPrice.setText(jTableDoctor.getValueAt(selectedRow, 4).toString());
            } catch (SQLException ex) {
                System.getLogger(DoctorPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                clearForm();
            }
        } else {
            JOptionPane.showMessageDialog(this, "'"+id+"' "+"doesn't exist!");
            jTextFieldSearch.setText("");
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed



    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        String type = jTextFieldType.getText();
        String age = jTextFieldAge.getText();
        String weight = jTextFieldWeight.getText();
        String clientId = jTextFieldClientID.getText().trim();
        Boolean gender = null;
        if (jComboBoxGender.getSelectedItem().toString().equals("Jantan")) {
            gender = true;
        } else {
            gender = false;
        }
        
        if (type.isEmpty() || age.isEmpty() || weight.isEmpty() || clientId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all field, including the Owner Client ID!");
            return;
        }
        
        Client owner = clientController.getById(clientId);
        if (owner == null) {
            JOptionPane.showMessageDialog(this, "No client found with ID '" + clientId + "'. Please enter a valid Client ID!");
            return;
        }
        
        animal = new Animal("",type,Integer.valueOf(age),Double.valueOf(weight),gender,owner);
        
        if (selectedId != null) {
            try {
                animalController.update(animal, selectedId);
                JOptionPane.showMessageDialog(this, "Animal updated successfully!");
            } catch (SQLException ex) {
                System.getLogger(AnimalPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                JOptionPane.showMessageDialog(this, "Failed to update animal!");
            }
        } else {
            try {
                animalController.create(animal);
            } catch (SQLException ex) {
                System.getLogger(AnimalPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                JOptionPane.showMessageDialog(this, "Failed to create animal!");
            }
        }
        
        clearForm();
        try {
            loadAnimalsData(limit, offset);
        } catch (SQLException ex) {
            System.getLogger(AnimalPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedId != null) {
            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure want to delete this animal?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
        
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    animalController.delete(selectedId);
                } catch (SQLException ex) {
                    System.getLogger(AnimalPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                }
                clearForm();
                try {
                    loadAnimalsData(limit,offset);
                    JOptionPane.showMessageDialog(this, "Animal deleted successfull!");
                } catch (SQLException ex) {
                    System.getLogger(AnimalPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                    JOptionPane.showMessageDialog(this, "Deleted unsuccessfull!");
                }
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed


    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        String name = jTextFieldName.getText();
        String address = jTextFieldAddress.getText();
        String telephone = jTextFieldTelephone.getText();
        
        if (name.isEmpty() || address.isEmpty() || telephone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all field!");
            return;
        }
        
        client = new Client("",name,address,telephone,null);
        
        if (selectedId != null) {
            try {
                clientController.update(client, selectedId);
                JOptionPane.showMessageDialog(this, "Client updated successfully!");
            } catch (SQLException ex) {
                System.getLogger(ClientPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                JOptionPane.showMessageDialog(this, "Failed to update client!");
            }
        } else {
            try {
                clientController.create(client);
            } catch (SQLException ex) {
                System.getLogger(ClientPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                JOptionPane.showMessageDialog(this, "Failed to create client!");
            }
        }
        
        clearForm();
        try {
            loadClientsData(limit, offset);
        } catch (SQLException ex) {
            System.getLogger(ClientPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        
    }//GEN-LAST:event_jButtonSaveActionPerformed


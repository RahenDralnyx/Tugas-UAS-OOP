    private void loadDoctorsData(int limit, int offset) throws SQLException {
        doctors = doctorController.getAll();
        List<Doctor> doctors_temp = doctorController.getSome(limit, offset);
        String[][] data = new String[doctors_temp.size()][5];
        
        for (int i=0; i<doctors_temp.size(); i++) {
            data[i][0] = doctors_temp.get(i).getIdDoctor();
            data[i][1] = doctors_temp.get(i).getName();
            data[i][2] = doctors_temp.get(i).getAddress();
            data[i][3] = doctors_temp.get(i).getTelephone();
            data[i][4] = String.valueOf(doctors_temp.get(i).getPrice());
        }
        
        String[] column = {"ID","Name","Address","Telephone","Price"};
        jTableDoctor.setModel(new DefaultTableModel(data,column) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }  
        });
        
        jLabelTotal.setText(String.valueOf(doctors.size()));
    }
    

    private void loadAnimalsData(int limit, int offset) throws SQLException {
        animals = animalController.getAll();
        List<Animal> animals_temp = animalController.getSome(limit, offset);
        String[][] data = new String[animals_temp.size()][6];
        
        for (int i=0; i<animals_temp.size(); i++) {
            data[i][0] = animals_temp.get(i).getIdAnimal();
            data[i][1] = animals_temp.get(i).getType();
            data[i][2] = String.valueOf(animals_temp.get(i).getAge());
            data[i][3] = String.valueOf(animals_temp.get(i).getWeight());
            Boolean gender_temp = animals_temp.get(i).getGender();
            if (gender_temp == false) {
                data[i][4] = gender[0];
            } else {
                data[i][4] = gender[1];
            }
            Client ownerTemp = animals_temp.get(i).getClient();
            data[i][5] = (ownerTemp != null && ownerTemp.getIdClient() != null && !ownerTemp.getIdClient().isEmpty())
                    ? ownerTemp.getIdClient() : "(none)";
        }
        
        String[] column = {"ID","Type","Age","Weight","Gender","Owner Client ID"};
        jTableAnimal.setModel(new DefaultTableModel(data,column) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }  
        });
        
        jLabelTotal.setText(String.valueOf(animals.size()));
    }
    

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedId != null) {
            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure want to delete this client?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
        
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    clientController.delete(selectedId);
                } catch (SQLException ex) {
                    System.getLogger(ClientPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                }
                clearForm();
                try {
                    loadClientsData(limit,offset);
                    JOptionPane.showMessageDialog(this, "Client deleted successfull!");
                } catch (SQLException ex) {
                    System.getLogger(ClientPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                    JOptionPane.showMessageDialog(this, "Deleted unsuccessfull!");
                }
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed


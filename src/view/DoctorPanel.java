    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        String name = jTextFieldName.getText();
        String address = jTextFieldAddress.getText();
        String telephone = jTextFieldTelephone.getText();
        String price = jTextFieldPrice.getText();
        
        if (name.isEmpty() || address.isEmpty() || telephone.isEmpty() || price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all field!");
            return;
        }
        
        doctor = new Doctor("",name,address,telephone,Double.valueOf(price));
        
        if (selectedId != null) {
            try {
                doctorController.update(doctor, selectedId);
                JOptionPane.showMessageDialog(this, "Client updated successfully!");
            } catch (SQLException ex) {
                System.getLogger(DoctorPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                JOptionPane.showMessageDialog(this, "Failed to update doctor!");
            }
        } else {
            try {
                doctorController.create(doctor);
            } catch (SQLException ex) {
                System.getLogger(DoctorPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                JOptionPane.showMessageDialog(this, "Failed to create doctor!");
            }
        }
        
        clearForm();
        try {
            loadDoctorsData(limit, offset);
        } catch (SQLException ex) {
            System.getLogger(DoctorPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedId != null) {
            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure want to delete this doctor?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
        
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    doctorController.delete(selectedId);
                } catch (SQLException ex) {
                    System.getLogger(DoctorPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                }
                clearForm();
                try {
                    loadDoctorsData(limit,offset);
                    JOptionPane.showMessageDialog(this, "Doctor deleted successfull!");
                } catch (SQLException ex) {
                    System.getLogger(ClientPanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                    JOptionPane.showMessageDialog(this, "Deleted unsuccessfull!");
                }
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed


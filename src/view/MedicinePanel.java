    private void loadMedicinesData(int limit, int offset) throws SQLException {
        medicines = medicineController.getAll();
        List<Medicine> medicines_temp = medicineController.getSome(limit, offset);
        String[][] data = new String[medicines_temp.size()][4];
        
        for (int i=0; i<medicines_temp.size(); i++) {
            data[i][0] = medicines_temp.get(i).getIdMedicine();
            data[i][1] = medicines_temp.get(i).getName();
            data[i][2] = String.valueOf(medicines_temp.get(i).getPrice());
            data[i][3] = String.valueOf(medicines_temp.get(i).getStock());
        }
        
        String[] column = {"ID","Name","Price","Stock"};
        jTableMedicine.setModel(new DefaultTableModel(data,column) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }  
        });
        
        jLabelTotal.setText(String.valueOf(medicines.size()));
    }
    

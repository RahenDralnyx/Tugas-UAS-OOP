    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        String id = jTextFieldSearch.getText();
        Boolean found = false;
        int size = medicines.size();
        int index = 0;
        for (Medicine medicine : medicines) {
            if (id.equals(medicine.getIdMedicine())) {
                found = true;
                break;
            }
            index++;
        }
        if (found) {
            try {
                index = ((size-1)-index);
                int page = (index/limit)+1;
                int row = index%limit;
                jLabelPage.setText("Page : "+String.valueOf(page));
                offset = (index/limit)*limit;
                loadMedicinesData(limit, offset);
                jTableMedicine.setRowSelectionInterval(row, row);
                
                int selectedRow = jTableMedicine.getSelectedRow();
                selectedId = jTableMedicine.getValueAt(selectedRow, 0).toString();
                jTextFieldName.setText(jTableMedicine.getValueAt(selectedRow, 1).toString());
                jTextFieldPrice.setText(jTableMedicine.getValueAt(selectedRow, 2).toString());
                jTextFieldQty.setText(jTableMedicine.getValueAt(selectedRow, 3).toString());
            } catch (SQLException ex) {
                System.getLogger(MedicinePanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                clearForm();
            }
        } else {
            JOptionPane.showMessageDialog(this, "'"+id+"' "+"doesn't exist!");
            jTextFieldSearch.setText("");
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed


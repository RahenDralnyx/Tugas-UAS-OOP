    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        String name = jTextFieldName.getText();
        String price = jTextFieldPrice.getText();
        String qty = jTextFieldQty.getText();
        
        if (name.isEmpty() || price.isEmpty() || qty.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all field!");
            return;
        }
        
        medicine = new Medicine("",name,Double.parseDouble(price),Integer.parseInt(qty));
        
        if (selectedId != null) {
            try {
                medicineController.update(medicine, selectedId);
                JOptionPane.showMessageDialog(this, "Medicine updated successfully!");
            } catch (SQLException ex) {
                System.getLogger(MedicinePanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                JOptionPane.showMessageDialog(this, "Failed to update medicine!");
            }
        } else {
            try {
                medicineController.create(medicine);
            } catch (SQLException ex) {
                System.getLogger(MedicinePanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                JOptionPane.showMessageDialog(this, "Failed to create medicine!");
            }
        }
        
        clearForm();
        try {
            loadMedicinesData(limit, offset);
        } catch (SQLException ex) {
            System.getLogger(MedicinePanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedId != null) {
            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure want to delete this medicine?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);
        
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    medicineController.delete(selectedId);
                } catch (SQLException ex) {
                    System.getLogger(MedicinePanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                }
                clearForm();
                try {
                    loadMedicinesData(limit,offset);
                    JOptionPane.showMessageDialog(this, "Medicine deleted successfull!");
                } catch (SQLException ex) {
                    System.getLogger(MedicinePanel.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                    JOptionPane.showMessageDialog(this, "Deleted unsuccessfull!");
                }
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

